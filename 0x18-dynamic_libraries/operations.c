#include "ops.h"

/**
 * add - adds two integers
 * @x: first integer
 * @y: second integer
 *
 * Return: sum of the two integers
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - subtracts two integers
 * @x: first integer
 * @y: second integer
 *
 *
 * Return: difference of the two
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - multiplies two integers
 * @x: first integer
 * @y: second integer
 *
 *
 * Return: product of the two
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - divides two integers
 * @x: first integer
 * @y: second integer
 * Return: division of x and y
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - finds the remainder
 * @x: first integer
 * @y: second integer
 * Return: the remainder of the two
 */
int mod(int x, int y)
{
	return (x % y);
}
